const translations = shared;
const I18nContext = createContext(null);

export function I18nProvider({ children }) {
  const [language, setLanguage] = useState(() => localStorage.getItem(STORAGE_KEY) || "ru");

  useEffect(() => {
    const normalizedLanguage = languages.includes(language) ? language : "ru";
    document.documentElement.lang = normalizedLanguage;
    localStorage.setItem(STORAGE_KEY, normalizedLanguage);
  }, [language]);

  const value = useMemo(
    () => ({
      language,
      languages,
      setLanguage,
      t(key, fallback = key) {
        return translations[language]?.[key] || fallback;
      },
      tx(value) {
        return typeof value === "string" ? translations[language]?.[value] || value : value;
      },
    }),
    [language],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);

  if (!context) {
    throw new Error("useI18n must be used inside I18nProvider");
  }

  return context;
}
